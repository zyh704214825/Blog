package com.ambow.javabean;

import java.util.Date;

public class Blogarticles extends Page{
	private int blog_id;
	private int comm_count;
	private String blog_name;//博客标题
	private String blog_remark;//博客正文
	private Users blog_users;
	private Date blog_date;
	private String blog_memo;
	private Classify blog_cls;
	public int getBlog_id() {
		return blog_id;
	}
	public void setBlog_id(int blog_id) {
		this.blog_id = blog_id;
	}
	public int getComm_count() {
		return comm_count;
	}
	public void setComm_count(int comm_count) {
		this.comm_count = comm_count;
	}
	public String getBlog_name() {
		return blog_name;
	}
	public void setBlog_name(String blog_name) {
		this.blog_name = blog_name;
	}
	public String getBlog_remark() {
		return blog_remark;
	}
	public void setBlog_remark(String blog_remark) {
		this.blog_remark = blog_remark;
	}
	public Users getBlog_users() {
		return blog_users;
	}
	public void setBlog_users(Users blog_users) {
		this.blog_users = blog_users;
	}
	public Date getBlog_date() {
		return blog_date;
	}
	public void setBlog_date(Date blog_date) {
		this.blog_date = blog_date;
	}
	public String getBlog_memo() {
		return blog_memo;
	}
	public void setBlog_memo(String blog_memo) {
		this.blog_memo = blog_memo;
	}
	public Classify getBlog_cls() {
		return blog_cls;
	}
	public void setBlog_cls(Classify blog_cls) {
		this.blog_cls = blog_cls;
	}
	public Blogarticles() {
		super();
	}
	public Blogarticles(int blog_id, int comm_count, String blog_name,
			String blog_remark, Users blog_users, Date blog_date,
			String blog_memo, Classify blog_cls) {
		super();
		this.blog_id = blog_id;
		this.comm_count = comm_count;
		this.blog_name = blog_name;
		this.blog_remark = blog_remark;
		this.blog_users = blog_users;
		this.blog_date = blog_date;
		this.blog_memo = blog_memo;
		this.blog_cls = blog_cls;
	}
	@Override
	public String toString() {
		return "Blogarticles [blog_id=" + blog_id + ", comm_count="
				+ comm_count + ", blog_name=" + blog_name + ", blog_remark="
				+ blog_remark + ", blog_users=" + blog_users + ", blog_date="
				+ blog_date + ", blog_memo=" + blog_memo + ", blog_cls="
				+ blog_cls + "]";
	}
	
}
